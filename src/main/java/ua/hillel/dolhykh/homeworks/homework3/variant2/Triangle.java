package ua.hillel.dolhykh.homeworks.homework3.variant2;

class Triangle implements ShapeArea {
    private double base;
    private double height;

    public Triangle(double base, double height) {
        this.base = base;
        this.height = height;
    }

    @Override
    public double area() {
        return 0.5 * base * height;
    }
}